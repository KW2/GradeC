public class ListViewItem {

    private String recordName, majors, subjects, scores, grades, allGrade, majorGrade;
    private int id, allNum, majorNum;

    public int getId() { return id;}

    public void setId(int id) {this.id = id;}

    public String getRecordName() {return recordName;}

    public void setRecordName(String recordName) {this.recordName = recordName;}

    public String getMajors() {return majors;}

    public void setMajors(String majors) {this.majors = majors;}

    public String getSubjects() {return subjects;}

    public void setSubjects(String subjects) {this.subjects = subjects;}

    public String getScores() {return scores;}

    public void setScores(String scores) {this.scores = scores;}

    public String getGrades() {return grades;}

    public void setGrades(String grades) {this.grades = grades;}

    public String getAllGrade() {return allGrade;}

    public void setAllGrade(String allGrade) {this.allGrade = allGrade;}

    public String getMajorGrade() {return majorGrade;}

    public void setMajorGrade(String majorGrade) {this.majorGrade = majorGrade;}

    public int getAllNum() {return allNum;}

    public void setAllNum(int allNum) {this.allNum = allNum;}

    public int getMajorNum() {return majorNum;}

    public void setMajorNum(int majorNum) {this.majorNum = majorNum;}
}
